/** Highest cumulative the full schedule ever reaches (~3 B, modulo
 *  ≈152 wei rounding loss across 16,744 accounts). */
export const SCHEDULE_FINAL_CUMULATIVE_WEI: bigint =
  GENESIS_UNLOCK_SCHEDULE[GENESIS_UNLOCK_SCHEDULE.length - 1][2];

/** Last month index in the full schedule. */
export const SCHEDULE_FINAL_MONTH: number =
  GENESIS_UNLOCK_SCHEDULE[GENESIS_UNLOCK_SCHEDULE.length - 1][0];

/** Final cumulative under Singularity (post-forfeiture). Equal to
 *  GENESIS_PREMINE_QUAI − SINGULARITY_SKIP_QUAI ≈ 1.3328 B. */
export const POST_SINGULARITY_FINAL_CUMULATIVE_WEI: bigint =
  POST_SINGULARITY_UNLOCK_SCHEDULE[
    POST_SINGULARITY_UNLOCK_SCHEDULE.length - 1
  ][2];

/** Last month with any unlock activity under Singularity. After this,
 *  every remaining scheduled unlock belongs to a forfeit-listed account
 *  and is dropped on-chain. */
export const POST_SINGULARITY_LAST_UNLOCK_MONTH: number =
  POST_SINGULARITY_UNLOCK_SCHEDULE[
    POST_SINGULARITY_UNLOCK_SCHEDULE.length - 1
  ][0];

function dateToMonthIndex(iso: string): number {
  const ms =
    new Date(iso + "T00:00:00Z").getTime() -
    new Date(SCHEDULE_ANCHOR_DATE + "T00:00:00Z").getTime();
  if (ms < 0) return -1;
  return Math.floor(ms / (SCHEDULE_DAYS_PER_MONTH * 86_400_000));
}

function lookup(
  table: ReadonlyArray<readonly [number, bigint, bigint]>,
  monthIdx: number,
): bigint {
  if (monthIdx < 0) return 0n;
  let cumulative = 0n;
  for (const [m, , c] of table) {
    if (m > monthIdx) break;
    cumulative = c;
  }
  return cumulative;
}

/** Cumulative QUAI unlocked at the end of the schedule month containing
 *  `iso`, **assuming the Singularity Fork did not happen** (full schedule
 *  through m72). */
export function cumulativeUnlockedAt(iso: string): bigint {
  const m = dateToMonthIndex(iso);
  if (m >= SCHEDULE_FINAL_MONTH) return SCHEDULE_FINAL_CUMULATIVE_WEI;
  return lookup(GENESIS_UNLOCK_SCHEDULE, m);
}

/** Cumulative QUAI unlocked at the end of the schedule month containing
 *  `iso` under the **actual on-chain regime** — full schedule through m13,
 *  then forfeiture-filtered from m14 onward. Plateaus at ~1.3328 B for
 *  any date past 2029-01-08 (m48). */
export function cumulativeUnlockedPostSingularity(iso: string): bigint {
  const m = dateToMonthIndex(iso);
  if (m >= POST_SINGULARITY_LAST_UNLOCK_MONTH) {
    return POST_SINGULARITY_FINAL_CUMULATIVE_WEI;
  }
  return lookup(POST_SINGULARITY_UNLOCK_SCHEDULE, m);
}

/** Backwards-compat alias for the chart while it's still mid-rename. */
export const cumulativeUnlockedAtCapped = cumulativeUnlockedPostSingularity;

/** ISO date (YYYY-MM-DD UTC) of the start of schedule month `m`. */
export function dateOfScheduleMonth(m: number): string {
  const ms =
    new Date(SCHEDULE_ANCHOR_DATE + "T00:00:00Z").getTime() +
    m * SCHEDULE_DAYS_PER_MONTH * 86_400_000;
  return new Date(ms).toISOString().slice(0, 10);
}

