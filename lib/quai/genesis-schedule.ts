// QUAI genesis-unlock schedule, derived directly from go-quai's
// `params/genesis_alloc.json` + `calculateLockedBalances` logic in
// `params/genesis_alloc.go`. Each row is one calendar month from mainnet
// block 0 (2025-01-29). Months without unlocks are omitted.
//
// The Singularity Fork (2026-03-19, Prime block 1,530,500) does NOT cap a
// global cumulative — it applies per-account forfeiture. The protocol
// gates each monthly unlock with:
//
//     if PrimeTerminusNumber >= SingularityForkBlock &&
//        forfeitureAddresses[account.Address] {
//         continue  // skip this account's unlock
//     }
//
// (source: go-quai `core/state/gen_allocs.go::AddLockedBalances`).
// 2,957 forfeit addresses × residual schedules from m14 onward = 1,667.16 M
// QUAI permanently never minted, leaving the post-Singularity vested
// baseline at exactly 1,332.84 M QUAI.
//
// We therefore expose two parallel cumulative series:
//   • GENESIS_UNLOCK_SCHEDULE         — full schedule, no forfeiture
//   • POST_SINGULARITY_UNLOCK_SCHEDULE — forfeiture-applied at m≥14
//
// They share m0–m13 exactly, diverge starting m14, and end at m72=3 B vs
// m48=1,332.84 M respectively. Regenerate with
// `npx tsx scripts/generate-genesis-schedule.ts` whenever go-quai bumps
// the alloc or forfeiture file.
//
// go-quai schedule shapes:
//   • 0: empty
//   • 1: 100% lump sum at per-account `lumpSumMonth` (most accounts)
//   • 2: 25% lump @ TGE, 36 monthly unlocks starting month 13
//   • 3: 25% lump @ month 12, 36 monthly unlocks starting month 13

